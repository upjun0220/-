# [데이터 가공] 시계열 데이터를 모델 학습용 Window(50) 단위로 분할함
def create_sequences(data, seq_length):
    xs, ys = [], []
    for i in range(len(data) - seq_length):
        xs.append(data[i:(i + seq_length)]) # 과거 50개의 데이터 (Input)
        ys.append(data[i + seq_length])    # 다음 1개의 데이터 (Label)
    # 2주차 트러블슈팅 포인트: [Batch, Seq, Feature]를 위해 unsqueeze(-1) 적용
    return torch.tensor(xs).unsqueeze(-1), torch.tensor(ys).unsqueeze(-1)

# ---------------------------------------------------------
# 2. LSTM 모델 정의
# ---------------------------------------------------------
# [모델 구조] 장기 기억 유지(Cell State)를 통해 시계열 패턴을 추출하는 핵심 아키텍처
class RadarToyLSTM(nn.Module):
    def __init__(self, input_size=1, hidden_size=64, num_layers=2):
        super(RadarToyLSTM, self).__init__()
        # 2개 층의 LSTM을 쌓아 깊은 특징 추출이 가능하도록 설계
        self.lstm = nn.LSTM(input_size, hidden_size, num_layers, batch_first=True)
        # 마지막 Hidden State를 입력받아 최종 예측값을 도출하는 Linear 레이어
        self.fc = nn.Linear(hidden_size, 1)

    def forward(self, x):
        # out: 모든 타임스텝의 출력, _: 최종 Hidden/Cell State
        out, _ = self.lstm(x)
        # 마지막 타임스텝(-1)의 결과만을 사용하여 다음 신호를 예측함
        return self.fc(out[:, -1, :])

