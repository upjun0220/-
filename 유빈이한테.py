df = pd.read_csv(url, parse_dates=['timestamp'], index_col='timestamp')
df_resampled = df.resample('16h').mean() # 'H' -> 'h'로 변경 (경고 수정)

# 정규화 및 시퀀스 생성
scaler = StandardScaler()
scaled_data = scaler.fit_transform(df_resampled)

def create_sequences(data, window_size=3):
    sequences = []
    for i in range(len(data) - window_size):
        sequences.append(data[i:i+window_size])
    return np.array(sequences)

window_size = 3
X = create_sequences(scaled_data, window_size)
X_torch = torch.tensor(X, dtype=torch.float32).cuda() # GPU 할당

# 2. LSTM 오토인코더 모델 정의
class LSTMAutoencoder(nn.Module):
    def __init__(self, input_dim, hidden_dim, seq_len):
        super(LSTMAutoencoder, self).__init__()
        self.seq_len = seq_len # Store seq_len for use in forward
        # Encoder
        self.encoder = nn.LSTM(input_dim, hidden_dim, batch_first=True)
        # Decoder
        self.decoder_input = nn.Linear(hidden_dim, hidden_dim) # Project latent space
        self.decoder = nn.LSTM(hidden_dim, input_dim, batch_first=True)

    def forward(self, x):
        _, (hidden, _) = self.encoder(x)
        # hidden state is (num_layers * num_directions, batch_size, hidden_dim)
        # For a single-layer, unidirectional LSTM, this is (1, batch_size, hidden_dim)

        # Squeeze the first dimension to get (batch_size, hidden_dim)
        hidden = hidden.squeeze(0)

        # Pass through the decoder_input linear layer
        decoder_input_tensor = self.decoder_input(hidden) # (batch_size, hidden_dim)

        # Repeat the decoder_input_tensor `seq_len` times along a new dimension
        # to create the input for the decoder LSTM: (batch_size, seq_len, hidden_dim)
        decoder_input_repeated = decoder_input_tensor.unsqueeze(1).repeat(1, self.seq_len, 1)

        x_out, _ = self.decoder(decoder_input_repeated)
        return x_out

model = LSTMAutoencoder(1, 64, window_size).cuda()
