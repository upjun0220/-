# ---------------------------------------------------------
# 2. LSTM-Autoencoder 모델 설계
# ---------------------------------------------------------
# [구조 설계] 1주 차의 Reshaping 개념과 2주 차의 LSTM 메커니즘을 통합함
class LSTMAutoencoder(nn.Module):
    def __init__(self, seq_len=50, input_dim=1, hidden_dim=16):
        super(LSTMAutoencoder, self).__init__()
        self.seq_len = seq_len
        # Encoder: 입력 시퀀스의 전체적인 맥락을 hidden state 하나로 압축
        self.encoder = nn.LSTM(input_dim, hidden_dim, batch_first=True)
        # Decoder LSTM: 압축된 특징(Latent Vector)을 시퀀스 길이만큼 반복하여 다시 시간축으로 확장
        self.decoder_lstm = nn.LSTM(hidden_dim, hidden_dim, batch_first=True)
        self.decoder_linear = nn.Linear(hidden_dim, input_dim)

    def forward(self, x):
        # Hidden State를 추출하여 시퀀스의 압축된 핵심 정보를 얻음
        _, (hidden, _) = self.encoder(x)
        hidden = hidden[-1]
        # 압축된 정보를 시퀀스 길이(50)만큼 복사하여 재구성 준비
        repeated_hidden = hidden.unsqueeze(1).repeat(1, self.seq_len, 1)
        decoded, _ = self.decoder_lstm(repeated_hidden)
        return self.decoder_linear(decoded)

