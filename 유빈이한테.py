    plt.plot(reconstructed[0, :, 0], label='Reconstructed', color='red', linestyle='--', linewidth=2)

    # [Operational Interface] 능동형 전력 차단 트리거의 상태 표시
    status = "NORMAL" if mse < threshold else "ANOMALY DETECTED"
