import torch
import torch.nn as nn
import numpy as np
import matplotlib.pyplot as plt

# ---------------------------------------------------------
# 1. 데이터 생성 함수 (Sin 및 Complex 파형)
# ---------------------------------------------------------
# [설계 의도] 실제 레이더 신호(호흡, 심박 등)를 모사하기 위한 기초 시퀀스 생성
def get_data(mode='sin'):
    t = np.linspace(0, 100, 1000)
    if mode == 'sin':
        # 가장 기초적인 단일 주기 신호
        data = np.sin(t).astype(np.float32)
    else:
        # 두 개의 주파수가 합성된 복합 신호: 레이더 데이터의 복잡성을 테스트함
        data = (np.sin(0.5 * t) + 0.5 * np.cos(2.0 * t)).astype(np.float32)
    return data

# [데이터 가공] 시계열 데이터를 모델 학습용 Window(50) 단위로 분할함
def create_sequences(data, seq_length):
    xs, ys = [], []
    for i in range(len(data) - seq_length):
        xs.append(data[i:(i + seq_length)]) # 과거 50개의 데이터 (Input)
        ys.append(data[i + seq_length])    # 다음 1개의 데이터 (Label)
    # 2주차 트러블슈팅 포인트: [Batch, Seq, Feature]를 위해 unsqueeze(-1) 적용
    return torch.tensor(xs).unsqueeze(-1), torch.tensor(ys).unsqueeze(-1)

# ---------------------------------------------------------
# 2. LSTM 모델 정의
# ---------------------------------------------------------
# [모델 구조] 장기 기억 유지(Cell State)를 통해 시계열 패턴을 추출하는 핵심 아키텍처
class RadarToyLSTM(nn.Module):
    def __init__(self, input_size=1, hidden_size=64, num_layers=2):
        super(RadarToyLSTM, self).__init__()
        # 2개 층의 LSTM을 쌓아 깊은 특징 추출이 가능하도록 설계
        self.lstm = nn.LSTM(input_size, hidden_size, num_layers, batch_first=True)
        # 마지막 Hidden State를 입력받아 최종 예측값을 도출하는 Linear 레이어
        self.fc = nn.Linear(hidden_size, 1)

    def forward(self, x):
        # out: 모든 타임스텝의 출력, _: 최종 Hidden/Cell State
        out, _ = self.lstm(x)
        # 마지막 타임스텝(-1)의 결과만을 사용하여 다음 신호를 예측함
        return self.fc(out[:, -1, :])

# ---------------------------------------------------------
# 3. 학습 및 결과 추출 함수
# ---------------------------------------------------------
def train_and_predict(mode='sin'):
    # 1주차 트러블슈팅 적용: T4 GPU 가속을 위해 device 할당 명시
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    data = get_data(mode)
    X, y = create_sequences(data, seq_length=50)
    X, y = X.to(device), y.to(device)

    model = RadarToyLSTM().to(device)
    criterion = nn.MSELoss() # 예측 오차 계산용
    optimizer = torch.optim.Adam(model.parameters(), lr=0.01)

    # 모델 학습 루프
    for epoch in range(10):
        outputs = model(X)
        loss = criterion(outputs, y)
        optimizer.zero_grad()
        loss.backward()
        optimizer.step()

    # 평가 모드로 전환하여 예측 수행
    model.eval()
    with torch.no_grad():
        pred = model(X).cpu().numpy()
    return data[50:], pred

