class RadarLSTMAutoencoder(nn.Module):
    def __init__(self, seq_len, n_features):
        super(RadarLSTMAutoencoder, self).__init__()

        # Encoder: 시계열의 특징을 압축하여 잠재 벡터(Latent Vector) 생성
        self.encoder = nn.LSTM(n_features, 64, batch_first=True)
        self.bottleneck = nn.Linear(64, 32)

        # Decoder: 압축된 벡터로부터 원래의 시퀀스를 복원
        self.decoder_input = nn.Linear(32, 64)
        self.decoder = nn.LSTM(64, n_features, batch_first=True)

    def forward(self, x):
        # x shape: [Batch, Seq_Len, Features]
        _, (hidden, _) = self.encoder(x)
        latent = self.bottleneck(hidden[-1])

        # 복원을 위한 차원 확장 및 재구성
        x_recon = self.decoder_input(latent).repeat(x.size(1), 1, 1).transpose(0, 1)
        x_out, _ = self.decoder(x_recon)
        return x_out

