# 1. 데이터 생성 (LSTM 입력 형태: [batch, seq_len, input_dim])
# ---------------------------------------------------------
# [설계 의도] LSTM의 3차원 입력 규격을 충족하는 레이더 모사 데이터를 생성함
def generate_lstm_data(num_samples=1000, seq_len=50):
    normal_samples = []
    for _ in range(num_samples):
        # 다양한 위상(Phase) 변화를 주어 모델의 동적 패턴 학습 능력을 테스트함
        start = np.random.rand() * 2 * np.pi
        x = np.linspace(start, start + 10, seq_len)
        normal_samples.append(np.sin(x))

    # [이상 상황] 낙상이나 급격한 움직임에 의한 신호 왜곡(Spike)을 모사
    # 실제 레이더 센서에서 발생하는 돌발적인 반사파 변화를 시뮬레이션함
    start = np.random.rand() * 2 * np.pi
    x = np.linspace(start, start + 10, seq_len)
    anomaly_sample = np.sin(x)
    anomaly_sample[20:30] += 2.0  # 인위적인 에너지 급증(Spike) 유도

    # [차원 정렬] (Batch, Seq, Feature) 규격 준수: Feature=1 (단일 레이더 신호)
    normal_data = np.array(normal_samples, dtype=np.float32)[..., np.newaxis]
    anomaly_data = anomaly_sample.astype(np.float32)[np.newaxis, ..., np.newaxis]
    return normal_data, anomaly_data

normal_data, anomaly_data = generate_lstm_data()
train_loader = DataLoader(TensorDataset(torch.from_numpy(normal_data), torch.from_numpy(normal_data)),
                          batch_size=32, shuffle=True)

# ---------------------------------------------------------
# 2. LSTM-Autoencoder 모델 설계
# ---------------------------------------------------------
# [핵심 철학] 2주 차의 LSTM 시계열 분석 역량과 3주 차의 Autoencoder 압축 로직을 결합
class LSTMAutoencoder(nn.Module):
    def __init__(self, seq_len=50, input_dim=1, hidden_dim=16):
        super(LSTMAutoencoder, self).__init__()
        self.seq_len = seq_len

        # [Encoder] 50개의 타임스텝을 훑으며 시퀀스의 흐름을 16차원의 Latent Vector로 압축
        self.encoder = nn.LSTM(input_dim, hidden_dim, batch_first=True)

        # [Decoder LSTM] 압축된 벡터로부터 다시 50개의 타임스텝을 생성(복원)하는 역할
        self.decoder_lstm = nn.LSTM(hidden_dim, hidden_dim, batch_first=True)
        self.decoder_linear = nn.Linear(hidden_dim, input_dim)

    def forward(self, x):
        # Hidden State 추출: 시퀀스 전체의 특징이 요약된 정보
        _, (hidden, _) = self.encoder(x)
        hidden = hidden[-1]

        # [Repeat Vector] 압축된 정보를 시퀀스 길이(50)만큼 복제하여 시간축을 재생성함
        # 이 과정이 "흐름을 기억했다가 다시 그리는" 핵심 메커니즘임
        repeated_hidden = hidden.unsqueeze(1).repeat(1, self.seq_len, 1)

        decoded, _ = self.decoder_lstm(repeated_hidden)
        return self.decoder_linear(decoded)

# ---------------------------------------------------------
# 3. 모델 학습 (GPU 가속 및 최적화)
# ---------------------------------------------------------
device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
model = LSTMAutoencoder().to(device)
criterion = nn.MSELoss(reduction='none') # 샘플별 정밀 분석을 위해 reduction 제거
optimizer = torch.optim.Adam(model.parameters(), lr=0.01)

model.train()
for epoch in range(60):
    for batch_x, _ in train_loader:
        batch_x = batch_x.to(device)
        optimizer.zero_grad()
        output = model(batch_x)
        loss = criterion(output, batch_x).mean()
        loss.backward()
        optimizer.step()

# ---------------------------------------------------------
# 4. 임계치(Threshold) 설정: 3-Sigma Rule 적용
# ---------------------------------------------------------
# [Project Standard] μ + 3σ (평균 + 3*표준편차) 기준을 통한 자동 경계 수립
# 통계적으로 99.7%의 정상 데이터 범위를 정의하며, 이를 벗어나면 '사고'로 판정
model.eval()
with torch.no_grad():
    all_normal = torch.from_numpy(normal_data).to(device)
    recon_all = model(all_normal)
    mse_losses = torch.mean((recon_all - all_normal)**2, dim=[1, 2]).cpu().numpy()

    # 실시간 감시 시스템의 논리적 근거가 되는 임계값 산출
    threshold = np.mean(mse_losses) + 3 * np.std(mse_losses)

# ---------------------------------------------------------
# 5. 결과 시각화 및 실시간 감시 시뮬레이션
# ---------------------------------------------------------
def plot_result(input_data, title, threshold):
    model.eval()
    with torch.no_grad():
        input_tensor = torch.from_numpy(input_data).to(device)
        reconstructed = model(input_tensor).cpu().numpy()
        mse = np.mean((input_data - reconstructed)**2)

    # 원본(Blue/Black)과 복원(Red)의 일치도 확인
    # 정상 신호는 거의 겹치며, 이상 신호는 불일치(High MSE)가 발생함
    plt.plot(input_data[0, :, 0], label='Original', color='blue' if mse < threshold else 'black', linewidth=2)
    plt.plot(reconstructed[0, :, 0], label='Reconstructed', color='red', linestyle='--', linewidth=2)

    # [Operational Interface] 능동형 전력 차단 트리거의 상태 표시 시뮬레이션
    status = "NORMAL" if mse < threshold else "ANOMALY DETECTED"
    status_color = "green" if status == "NORMAL" else "red"

    # 상태에 따른 텍스트 출력 (정상은 하단, 이상은 상단에 배치하여 시인성 확보)
    y_pos = -0.8 if status == "NORMAL" else 2.5
    plt.text(2, y_pos, f"Status: {status}",
             bbox=dict(facecolor='white', alpha=0.8, edgecolor='gray', boxstyle='round,pad=0.5'),
             color=status_color, fontweight='bold', fontsize=12)

    plt.title(f"{title}\nMSE: {mse:.4f} (Threshold: {threshold:.4f})")
    plt.ylim(-1.5, 3.5)
    plt.legend(loc='upper right')

