# 2. LSTM-Autoencoder 모델 설계
class LSTMAutoencoder(nn.Module):
    def __init__(self, seq_len=50, input_dim=1, hidden_dim=16):
        super(LSTMAutoencoder, self).__init__()
        self.seq_len = seq_len

        # Encoder: 시계열 흐름 압축
        self.encoder = nn.LSTM(input_dim, hidden_dim, batch_first=True)

        # Decoder: 압축된 벡터로 시계열 복원
        self.decoder_lstm = nn.LSTM(hidden_dim, hidden_dim, batch_first=True)
        self.decoder_linear = nn.Linear(hidden_dim, input_dim) # 원래 차원으로 매핑

    def forward(self, x):
        # Encoder: 전체 시퀀스를 읽고 마지막 hidden state만 추출
        _, (hidden, _) = self.encoder(x)

        # Decoder: 추출된 핵심 특징(Latent)을 seq_len만큼 복제하여 입력으로 사용
        hidden = hidden[-1] # shape: (batch_size, hidden_dim)
        repeated_hidden = hidden.unsqueeze(1).repeat(1, self.seq_len, 1) # (batch, seq_len, hidden)

        decoded, _ = self.decoder_lstm(repeated_hidden)
        out = self.decoder_linear(decoded)
        return out

