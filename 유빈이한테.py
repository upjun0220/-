import torch
import torch.nn as nn

# 1. 모델 정의 (LSTMCell 활용)
class Sequence(nn.Module):
    def __init__(self):
        super(Sequence, self).__init__()
        # 영상의 핵심: 일반 LSTM 이 아닌 LSTMCell 을 2개 겹쳐서 사용
        self.lstm1 = nn.LSTMCell(1, 51)
        self.lstm2 = nn.LSTMCell(51, 51)
        self.linear = nn.Linear(51, 1)

    def forward(self, input, future=0):
        outputs = []

        # 초기 은닉 상태(Hidden State) 와 셀 상태(Cell State) 텐서 초기화
        h_t = torch.zeros(input.size(0), 51, dtype=torch.float32)
        c_t = torch.zeros(input.size(0), 51, dtype=torch.float32)
        h_t2 = torch.zeros(input.size(0), 51, dtype=torch.float32)
        c_t2 = torch.zeros(input.size(0), 51, dtype=torch.float32)

        # 1단계: 주어진 입력 데이터(Sine wave) 를 1스텝씩 쪼개서 학습
        for input_t in input.split(1, dim=1):
            h_t, c_t = self.lstm1(input_t, (h_t, c_t))
            h_t2, c_t2 = self.lstm2(h_t, (h_t2, c_t2))
            output = self.linear(h_t2)
            outputs.append(output)

        # 2단계: 학습된 패턴을 바탕으로 미래(Future) 시퀀스 예측
        # 이전 스텝의 출력값 이 다음 스텝의 입력값으로 다시 들어감 (Autoregressive)
        for i in range(future):
            h_t, c_t = self.lstm1(output, (h_t, c_t))
            h_t2, c_t2 = self.lstm2(h_t, (h_t2, c_t2))
            output = self.linear(h_t2)
            outputs.append(output)

        # 예측된 모든 스텝의 결과 를 하나의 텐서로 병합
        outputs = torch.cat(outputs, dim=1)
        return outputs

